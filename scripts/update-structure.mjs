#!/usr/bin/env node
/**
 * update-structure.mjs — regenerates STRUCTURE.md from the git index.
 *
 * Called by scripts/git-hooks/post-commit alongside update-features.mjs, so the
 * snapshot tracks every commit. Deterministic: the file only changes when the
 * tree actually changes. The placement RULES live in AGENTS.md ("File
 * organization rules") — this file is just the generated snapshot of reality.
 */
import { execFileSync } from "node:child_process";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

let repoRoot = "";
function git(...args) {
  return execFileSync("git", args, { encoding: "utf8", cwd: repoRoot || undefined });
}
repoRoot = git("rev-parse", "--show-toplevel").trim();
const OUT_PATH = join(repoRoot, "STRUCTURE.md");

const MAX_DEPTH = 3;

/** Top-level entries sanctioned by the AGENTS.md placement table. */
const EXPECTED_TOP = new Set([
  "frontend",
  "backend",
  "scripts",
  "docs",
  "tests",
  ".gitattributes",
  ".gitignore",
  "AGENTS.md",
  "CLAUDE.md",
  "FEATURES.md",
  "HOW_TO_RUN.md",
  "README.md",
  "SPEC.md",
  "STRUCTURE.md",
  "eslint.config.mjs",
  "package.json",
  "pnpm-lock.yaml",
  "pnpm-workspace.yaml",
]);

/** Directory annotations, mirroring the AGENTS.md rules table. */
const ANNOTATIONS = {
  frontend: "Next.js app — @green-diamond/frontend",
  "frontend/app": "routes (storefront + admin + api handlers)",
  "frontend/components": "presentational UI (ui/ = primitives)",
  "frontend/features": "domain modules (cart, checkout, …)",
  "frontend/lib": "client-only helpers & hooks",
  "frontend/public": "static assets",
  backend: "domain & data — @green-diamond/backend",
  "backend/src": "backend source (relative imports only)",
  "backend/src/services": "service interfaces + providers (mocks live, vendors deferred)",
  "backend/src/repositories": "data access (mock now → Prisma in Phase 3)",
  "backend/src/types": "shared domain types",
  "backend/src/validation": "zod schemas",
  "backend/src/config": "env, flags, money, tax",
  "backend/src/db": "prisma client/schema/seed (Phase 3)",
  "backend/src/shop": "catalog facet/query mapping",
  scripts: "repo automation (feature log, structure snapshot, hook setup)",
  "scripts/git-hooks": "committed git hooks (activated via core.hooksPath)",
  docs: "guides (RUNBOOK)",
  tests: "unit + e2e",
};

const files = git("ls-files").trim().split("\n").filter(Boolean);

// dir path -> total file count beneath it
const dirCounts = new Map();
const rootFiles = [];
for (const file of files) {
  const parts = file.split("/");
  if (parts.length === 1) {
    rootFiles.push(file);
    continue;
  }
  for (let d = 1; d < parts.length; d++) {
    const dir = parts.slice(0, d).join("/");
    dirCounts.set(dir, (dirCounts.get(dir) ?? 0) + 1);
  }
}

function childDirs(parent) {
  const depth = parent === "" ? 1 : parent.split("/").length + 1;
  return [...dirCounts.keys()]
    .filter((d) => d.split("/").length === depth && (parent === "" || d.startsWith(parent + "/")))
    .sort();
}

function renderDir(dir, lines) {
  const depth = dir.split("/").length;
  const indent = "  ".repeat(depth - 1);
  const name = dir.split("/").pop();
  const note = ANNOTATIONS[dir] ? ` — ${ANNOTATIONS[dir]}` : "";
  lines.push(`${indent}${name}/ (${dirCounts.get(dir)})${note}`);
  if (depth >= MAX_DEPTH) return;
  for (const child of childDirs(dir)) renderDir(child, lines);
}

const treeLines = [];
for (const top of childDirs("")) renderDir(top, treeLines);

const unexpected = [...new Set([...childDirs(""), ...rootFiles])].filter(
  (entry) => !EXPECTED_TOP.has(entry),
);

const out = `# Repository structure

> **Auto-generated by \`scripts/update-structure.mjs\` (post-commit hook) — do not edit.**
> Where files GO is defined in [AGENTS.md → "File organization rules"](./AGENTS.md);
> import boundaries are enforced by \`pnpm lint\`. This file is the tracked snapshot
> of the tree as of the latest commit.

\`\`\`
${treeLines.join("\n")}
\`\`\`

Root files: ${rootFiles.sort().map((f) => `\`${f}\``).join(", ")}
${
  unexpected.length
    ? `\n> ⚠ Unexpected top-level entr${unexpected.length === 1 ? "y" : "ies"}: ${unexpected
        .map((u) => `\`${u}\``)
        .join(", ")} — not in the AGENTS.md placement table. Move it, or update the table + \`EXPECTED_TOP\` in \`scripts/update-structure.mjs\`.\n`
    : ""
}`;

const before = existsSync(OUT_PATH) ? readFileSync(OUT_PATH, "utf8") : null;
if (out !== before) {
  writeFileSync(OUT_PATH, out);
  console.log(`STRUCTURE.md updated (${treeLines.length} directories).`);
} else {
  console.log("STRUCTURE.md already up to date.");
}
